/** This analysis prints the nodes of an AST as an indented tree
 * 
 * @author aldrich
 * @since Crystal 3.4.1
 */
public class PrintNodesAnalysis extends AbstractCrystalMethodAnalysis {

	@Override
	public void analyzeMethod(MethodDeclaration d) {
		PrintNodesVisitor v = new PrintNodesVisitor(getReporter().userOut());
		
		d.accept(v);
	}
}